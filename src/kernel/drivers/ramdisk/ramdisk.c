static void
ramdisk_driver_thread(void) {
	static Msg m;
	while (true) {
		receive(ANY, &m);
		switch (m.type) {
			case DEV_READ:
				copy_from_kernel(fetch_pcb(m.req_pid), m.buf, disk+m.offset, m.len);
				break;
			case DEV_WRITE:
				copy_to_kernel(fetch_pcb(m.req_pid), disk+m.offset, m.buf, m.len);
				break;
			default: assert(0);
		}
		m.dest=m.src;
		m.src=RAMDISK;
		send(m.dest,&m);
	}
}

